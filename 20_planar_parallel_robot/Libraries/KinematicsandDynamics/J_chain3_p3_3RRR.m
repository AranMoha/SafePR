function J_chain3_p3_3RRRreturn = J_chain3_p3_3RRR(qj_rad, psi_rad, l_all_id)
  Jac_chain3_p3 = [0 0; 0 0;];
  t1 = qj_rad(7);
  t2 = psi_rad(3);
  t3 = t1 + t2;
  t4 = sin(t3);
  t5 = l_all_id(5);
  t8 = t1 + qj_rad(8) + t2;
  t9 = sin(t8);
  t10 = l_all_id(6);
  t11 = t10 * t9;
  Jac_chain3_p3(1,1) = -t4 * t5 - t11;
  Jac_chain3_p3(1,2) = -t11;
  t12 = cos(t8);
  t13 = t10 * t12;
  t14 = cos(t3);
  Jac_chain3_p3(2,1) = t14 * t5 + t13;
  Jac_chain3_p3(2,2) = t13;
  J_chain3_p3_3RRRreturn = Jac_chain3_p3;
