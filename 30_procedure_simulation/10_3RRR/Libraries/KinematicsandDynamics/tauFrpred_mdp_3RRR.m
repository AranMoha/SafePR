  t6 = qj_rad(1);
  t7 = sin(t6);
  t9 = qj_rad(2);
  t10 = t6 + t9;
  t11 = sin(t10);
  t14 = sin(t9);
  t15 = 0.1e1 / t14;
  t17 = dqj_rad(2);
  t18 = sign(t17);
  t23 = t17 * mdp(7) + t18 * mdp(9);
  t26 = qj_rad(4);
  t27 = pi / 0.6e1;
  t28 = t26 + t27;
  t29 = cos(t28);
  t31 = qj_rad(5);
  t32 = t26 + t31 + t27;
  t33 = cos(t32);
  t36 = sin(t31);
  t37 = 0.1e1 / t36;
  t39 = dqj_rad(5);
  t40 = sign(t39);
  t45 = t39 * mdp(11) + t40 * mdp(13);
  t48 = qj_rad(7);
  t49 = qj_rad(8);
  t50 = pi / 0.3e1;
  t51 = t48 + t49 + t50;
  t52 = sin(t51);
  t54 = t48 + t50;
  t55 = sin(t54);
  t59 = sin(t49);
  t60 = 0.1e1 / t59;
