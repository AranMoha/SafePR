  t71 = cos(t6);
  t73 = cos(t10);
  t79 = sin(t28);
  t81 = sin(t32);
  t87 = cos(t54);
  t89 = cos(t51);
  tauFrpredT(1,2) = -t23 * t15 * (t1 * t71 + t3 * t73) * t5 + t45 * t37 * (t1 * t79 + t3 * t81) * t5 + t68 * t60 * (t1 * t87 + t3 * t89) * t5;
  t95 = r_BasisPla_MobPla(2);
  t98 = xEE(6);
  t100 = cos(t6 - t98 + t9);
  t103 = cos(-t98 + t6);
  t110 = cos(t26 - t98);
  t113 = cos(t26 + t31 - t98);
  t122 = cos(t48 - t98);
  t125 = cos(t48 + t49 - t98);
  tauFrpredT(1,3) = -t23 * t15 * (t1 * t103 + t100 * t3) * t4 * t2 * t95 - t45 * t37 * t4 * t2 * (t1 * t110 + t113 * t3) * t95 - t69 * t2 * (t1 * t122 + t125 * t3) * t95;
  tauFrpred_mdp_3RRRreturn = tauFrpredT;
