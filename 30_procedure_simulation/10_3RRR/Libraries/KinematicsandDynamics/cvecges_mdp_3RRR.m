function cvecges_mdp_3RRRreturn = cvecges_mdp_3RRR(qj_rad, dqj_rad, xEE, dxEE, mdp, l_all, r_BasisPla_MobPla)
  cvecgesT = [0 0 0 0 0 0 0 0 0;];
  t2 = sin(qj_rad(2));
  t3 = dqj_rad(2);
  t5 = dqj_rad(1);
  t8 = mdp(3);
  cvecgesT(1,1) = -t8 * (2 * t5 + t3) * t3 * t2;
  t11 = t5 ^ 2;
  cvecgesT(1,2) = t8 * t11 * t2;
  t14 = sin(qj_rad(5));
  t15 = dqj_rad(5);
  t17 = dqj_rad(4);
  cvecgesT(1,3) = -t8 * (2 * t17 + t15) * t15 * t14;
  t22 = t17 ^ 2;
  cvecgesT(1,4) = t8 * t22 * t14;
  t25 = sin(qj_rad(8));
  t26 = dqj_rad(8);
  t28 = dqj_rad(7);
  cvecgesT(1,5) = -t8 * (2 * t28 + t26) * t26 * t25;
  t33 = t28 ^ 2;
  cvecgesT(1,6) = t8 * t33 * t25;
  cvecgesT(1,7) = 0.0e0;
  cvecgesT(1,8) = 0.0e0;
  cvecgesT(1,9) = 0.0e0;
  cvecges_mdp_3RRRreturn = cvecgesT;
