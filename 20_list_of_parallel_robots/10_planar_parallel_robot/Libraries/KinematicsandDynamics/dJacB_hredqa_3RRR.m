function dJacB_hredqa_3RRRreturn = dJacB_hredqa_3RRR(qj_rad, dqj_rad, xEE, dxEE, l_all, r_BasisPla_MobPla)
  dJacB_hredqa = [0 0 0; 0 0 0; 0 0 0;];
  t1 = l_all(1);
  t2 = xEE(6);
  t3 = qj_rad(1);
  t5 = sin(-t2 + t3);
  t6 = r_BasisPla_MobPla(2);
  t8 = sin(t3);
  t9 = r_BasisPla_MobPla(1);
  t11 = xEE(1);
  t13 = cos(t3);
  t14 = xEE(2);
  t21 = dxEE(1);
  t24 = dxEE(2);
  t28 = dxEE(6) * t6;
  dJacB_hredqa(1,1) = 0.2e1 * dqj_rad(1) * (-t11 * t8 + t13 * t14 - t5 * t6 + t8 * t9) * t1 + 0.2e1 * t21 * t1 * t13 + 0.2e1 * t24 * t1 * t8 + 0.2e1 * t28 * t1 * t5;
  dJacB_hredqa(1,2) = 0.0e0;
  dJacB_hredqa(1,3) = 0.0e0;
  dJacB_hredqa(2,1) = 0.0e0;
  t31 = qj_rad(4);
  t33 = sin(-t2 + t31);
  t36 = t31 + pi / 0.6e1;
  t37 = sin(t36);
  t39 = cos(t36);
  t41 = sin(t31);
  dJacB_hredqa(2,2) = 0.2e1 * dqj_rad(4) * (-t11 * t39 - t14 * t37 - t33 * t6 + t41 * t9) * t1 - 0.2e1 * t21 * t1 * t37 + 0.2e1 * t24 * t1 * t39 + 0.2e1 * t28 * t1 * t33;
  dJacB_hredqa(2,3) = 0.0e0;
  dJacB_hredqa(3,1) = 0.0e0;
  dJacB_hredqa(3,2) = 0.0e0;
  t54 = qj_rad(7);
  t56 = sin(-t2 + t54);
  t59 = t54 + pi / 0.3e1;
  t60 = sin(t59);
  t62 = cos(t59);
  t64 = sin(t54);
  dJacB_hredqa(3,3) = 0.2e1 * dqj_rad(7) * (t11 * t60 - t14 * t62 - t56 * t6 + t64 * t9) * t1 - 0.2e1 * t21 * t1 * t62 - 0.2e1 * t24 * t1 * t60 + 0.2e1 * t28 * t1 * t56;
  dJacB_hredqa_3RRRreturn = dJacB_hredqa;
